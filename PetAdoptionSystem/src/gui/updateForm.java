    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
       String newPetName = txtNewPetName.getText().trim();
        String newSpecies = txtNewSpecies.getText().trim();
        String newBreed = txtNewBreed.getText().trim();
        String ageText = txtNewAge.getText().trim();
        String newStatus = cmbStatus.getSelectedItem().toString();

        // Input validation
        if (newPetName.isEmpty() || newSpecies.isEmpty() || newBreed.isEmpty() || ageText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }

        int newAge;
        try {
            newAge = Integer.parseInt(ageText);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid number for age.");
            return;
        }

        // Update in database
        try (Connection conn = ConnectionMySQL.getConnection();  // Your DB class
             PreparedStatement stmt = conn.prepareStatement(
                 "UPDATE pets SET PetName=?, Species=?, Breed=?, Age=?, Status=? WHERE PetID=?")) {

            stmt.setString(1, newPetName);
            stmt.setString(2, newSpecies);
            stmt.setString(3, newBreed);
            stmt.setInt(4, newAge);
            stmt.setString(5, newStatus);
            stmt.setInt(6, selectedPetId);

            int rowsAffected = stmt.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Pet updated successfully!");

                // If parentFrame is passed and accessible, refresh the table
                // âœ… Refresh table in the main petFrame
                if (parentFrame != null) {
                    parentFrame.loadPetData();
                }

                this.dispose(); // Close updateForm
                
            } else {
                JOptionPane.showMessageDialog(this, "Update failed. Pet not found.");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_submitButtonActionPerformed

